        <h3 class="page-header">Apa itu PIP?</h3>
        <p>PIP (Program Indonesia Pintar) adalah pemberian bantuan tunai pendidikan yang diperuntukkan bagi siswa Sekolah Dasar Negeri</p>
        <p>Penerima merupakan pemilik Kartu Keluarga Sejahtera (KKS), peserta Program Keluarga Harapan (PKH), yatim piatu, penyandang disabilitas, korban bencana alam/musibah. </p>
        <p>Tujuan dari program ini adalah untuk membantu biaya personal pendidikan bagi peserta didik miskin atau rentan miskin yang masih terdaftar sebagai peserta didik.</p>
        <p>Peserta didik menerima dana bantuan PIP sebanyak 1 (satu) kali dalam 1 (satu) tahun anggaran yakni sejumlah Rp. 450.000,-/tahun.</p>
      

